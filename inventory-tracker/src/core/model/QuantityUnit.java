package core.model;

/**
 * The {@code QuantityUnit} enumeration is used to designate the unit of 
 * measurement for a particular quantity.
 * 
 * @author kemcqueen
 */
public enum QuantityUnit {
    COUNT,
    POUNDS,
    OUNCES,
    GRAMS,
    KILOGRAMS,
    GALLONS,
    QUARTS,
    PINTS,
    FLUID_OUNCES,
    LITERS;
}
